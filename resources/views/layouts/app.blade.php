<script>
    document.addEventListener('DOMContentLoaded', function () {
        const addStatusButton = document.getElementById('addStatusButton');
        const statusInput = document.getElementById('statusInput');
        const statusValueInput = document.getElementById('statusValue');
        const saveStatusButton = document.getElementById('saveStatusButton');

        addStatusButton.addEventListener('click', function () {
            statusInput.style.display = 'block';
        });

        saveStatusButton.addEventListener('click', function () {
            const statusSelect = document.getElementById('status');
            const newStatusOption = document.createElement('option');
            newStatusOption.value = statusValueInput.value;
            newStatusOption.textContent = statusValueInput.value;
            statusSelect.appendChild(newStatusOption);
            statusValueInput.value = ''; // Clear the input field
            statusInput.style.display = 'none';
        });
    });
</script>

